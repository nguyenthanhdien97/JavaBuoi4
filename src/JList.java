    private void getListItems(String type) { 
        // dung array list chua cac gia tri trong danh sach JList 
        ArrayList<Integer> arrList = new ArrayList<Integer>(); 
 
        if (type.equals("EVEN")) { 
            for (int i = 0; i < modelList.size(); i++) 
            { 
                int item = Integer.parseInt(modelList.get(i).toString()); 
                if (item % 2 == 0) { 
                    arrList.add(i); 
                } 
            } 
            selectedItemList = new int[arrList.size()]; 
 
            for (int i = 0; i < arrList.size(); i++) { 
                selectedItemList[i] = arrList.get(i); 
            } 
        } else if (type.equals("PRIME")) { 
            for (int i = 0; i < modelList.size(); i++) 
            { 
                int item = Integer.parseInt(modelList.get(i).toString()); 
                if (isPrime(item)) { 
                    arrList.add(i); 
                } 
            } 
            selectedItemList = new int[arrList.size()]; 
             
            for (int i = 0; i < arrList.size(); i++) { 
                selectedItemList[i] = arrList.get(i); 
            } 
        } else if (type.equals("ODD")) { 
            for (int i = 0; i < modelList.size(); i++) 
            { 
                int item = Integer.parseInt(modelList.get(i).toString()); 
                if (item % 2 == 1) { 
                    arrList.add(i); 
                } 
            } 
            selectedItemList = new int[arrList.size()]; 
 
            for (int i = 0; i < arrList.size(); i++) { 
                selectedItemList[i] = arrList.get(i); 
            } 
        } 
    } 
    private boolean isPrime(int number) { 
            if (number <= 1) { 
                return false; 
            } 

            for (int i = 2; i < number; i++) { 
                if (number % i == 0) { 
                    return false; 
                } 
            } 
            return true; 
    }
